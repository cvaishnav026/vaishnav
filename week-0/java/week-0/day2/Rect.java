import java.lang.*;

class Rect
{
	public static void main(String ar[])
	{
		int l=500,w=200,A,P;
		A=l*w;
		P=2*(l+w);
		System.out.println("The length of rect is "+l);
		System.out.println("\nThe width of rect is "+w);
		System.out.println("\nThe area of rect is "+A);
		System.out.println("\nThe perimeter of rect is "+P);
	
	}
}